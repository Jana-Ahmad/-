let counter1 = 0;
const counterClick1 = document.querySelector(".counter1 .counter-click");
const resetClick1 = document.querySelector(".counter1 .reset");

counterClick1.onclick = () => {
  counter1 += 1;
  document.querySelector(".counter1 .counter-display ").innerHTML = counter1;
};
resetClick1.onclick = () => {
  counter1 = 0;
  document.querySelector(".counter1 .counter-display ").innerHTML = counter1;
};

